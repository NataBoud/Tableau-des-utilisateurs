function searchTable(value, data){
    var filteredData = []

    for (var i = 0; i < data.length; i++){
        value = value.toLowerCase()
        var member = data[i].member.toLowerCase()

        if (member.includes(value)){
            filteredData.push(data[i])
        }
    }
    return filteredData 
}

